public class RacunskeOperacije {
    public int sabiranje(int a, int b) {
        return a + b;
    }

    public int oduzimanje(int a, int b) {
        return a - b;
    }

    public int mnozenje(int a, int b) {
        return a * b;
    }

    public double dijeljenje(double a, double b) {
        return a / b;
    }
}
